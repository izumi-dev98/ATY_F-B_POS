export function getResolvedAccess(user) {
  if (!user) return [];
  if (user.role === "superadmin") return ROLE_ACCESS_RIGHTS.superadmin;
  if (Array.isArray(user.permissions) && user.permissions.length > 0) return user.permissions;
  return ROLE_ACCESS_RIGHTS[user.role] || [];
}

export function hasFeature(user, feature) {
  if (user?.role === "superadmin") return true;
  const access = getResolvedAccess(user);
  if (access.includes(feature)) return true;
  const fallback = featureFallback[feature] || [];
  return fallback.some((fb) => access.includes(fb));
}
